void main() {
  // Built-in-DataTypes
  int cart_item = 10;
  print(cart_item);

  double price = 10.50;
  print(price);

  String city = "Ranchi";
  print(city);

  bool is_completed = true;
  print(is_completed);

  bool is_login = false;
  print(is_login);

  var quantity = 100;
  print(quantity);

  // float, String, Boolean we can also use
  var cost = 100.50;
  print(cost);

  var gender = "Male";
  print(gender);

  var is_full = true;
  print(is_full);

  // # Check Data Type
  // here runtimeType is used to find the datatypes of varibles.
  print(cart_item.runtimeType);
  print(price.runtimeType);
  print(city.runtimeType);
  print(is_completed.runtimeType);
  print(is_login.runtimeType);
  print(quantity.runtimeType);
  print(cost.runtimeType);
  print(gender.runtimeType);
  print(is_full.runtimeType);
}
